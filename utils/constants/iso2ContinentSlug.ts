function invertIso2ToContinent(): Record<ContinentSlug, string[]> {
  const buckets: Record<ContinentSlug, string[]> = { af: [], an: [], as: [], eu: [], na: [], oc: [], sa: [] }
  for (const [iso2, c] of Object.entries(ISO2_TO_CONTINENT_SLUG) as [string, ContinentSlug][]) {
    buckets[c].push(iso2)
  }
  return buckets
}

